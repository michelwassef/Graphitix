const fs = require('fs');
const path = require('path');

const ROOT = process.cwd();
const COMPONENTS_JS = path.join(ROOT, 'js', 'main', 'components.js');
const OUTPUT_PATH = path.join(ROOT, 'docs', 'development', 'component-contracts.md');

const PAYLOAD_BASELINES = {
  venn: ['type', 'data', 'style', 'notes', 'analysis'],
  box: ['type', 'data', 'exclusions', 'config'],
  scatter: ['type', 'data', 'exclusions', 'series', 'config'],
  pca: ['type', 'data', 'exclusions', 'stats', 'config'],
  line: ['type', 'data', 'exclusions', 'series', 'config'],
  heatmap: ['type', 'data', 'exclusions', 'config'],
  surface: ['type', 'data', 'exclusions', 'config'],
  roc: ['type', 'data', 'exclusions', 'stats', 'config'],
  survival: ['type', 'data', 'exclusions', 'stats', 'config'],
  hist: ['type', 'data', 'exclusions', 'config'],
  pie: ['type', 'data', 'exclusions', 'config']
};

const REQUIRED_METHODS = ['ensure', 'draw', 'getPayload', 'loadFromPayload', 'createEmptyPayload'];
const KNOWN_HOOKS = new Set([
  'ensure',
  'draw',
  'getPayload',
  'loadFromFile',
  'loadFromPayload',
  'applyColorSchemePayload',
  'createEmptyPayload',
  'activateTab',
  'captureRuntimeState',
  'applyRuntimeState',
  'captureRenderCache',
  'restoreRenderCache',
  'getLayoutState',
  'getDefaultLayoutState',
  'applyLayoutState'
]);

function toPosix(relativePath) {
  return relativePath.split(path.sep).join('/');
}

function countChar(text, needle) {
  let count = 0;
  for (let i = 0; i < text.length; i += 1) {
    if (text[i] === needle) count += 1;
  }
  return count;
}

function parseComponentsRegistry(lines) {
  const bundleMap = new Map();
  const bundleRegex = /^\s*([a-z][a-z0-9_]*):\s*\{\s*browserPath:\s*'([^']+)',\s*requirePath:\s*'([^']+)'\s*\},?\s*$/i;
  for (let i = 0; i < lines.length; i += 1) {
    const match = lines[i].match(bundleRegex);
    if (!match) continue;
    bundleMap.set(match[1].toLowerCase(), {
      type: match[1].toLowerCase(),
      browserPath: match[2],
      requirePath: match[3],
      line: i + 1
    });
  }

  const start = lines.findIndex(line => line.includes('const WORKSPACES = {'));
  if (start < 0) return [];
  const entries = [];
  let inRegistry = false;
  let registryDepth = 0;
  let current = null;
  let currentDepth = 0;

  for (let i = start; i < lines.length; i += 1) {
    const line = lines[i];
    if (!inRegistry) {
      inRegistry = true;
      registryDepth = countChar(line, '{') - countChar(line, '}');
      continue;
    }

    if (!current) {
      const match = line.match(/^\s{4}([a-z][a-z0-9_]*):\s*\{\s*$/i);
      if (match) {
        current = {
          key: match[1].toLowerCase(),
          startLine: i + 1,
          endLine: i + 1,
          type: null,
          tabLabel: null,
          elementId: null,
          hooks: []
        };
        currentDepth = countChar(line, '{') - countChar(line, '}');
      }
    } else {
      currentDepth += countChar(line, '{') - countChar(line, '}');
      current.endLine = i + 1;
      const typeMatch = line.match(/^\s{6}type:\s*'([^']+)'/);
      if (typeMatch) current.type = typeMatch[1];
      const labelMatch = line.match(/^\s{6}tabLabel:\s*'([^']+)'/);
      if (labelMatch) current.tabLabel = labelMatch[1];
      const elementMatch = line.match(/^\s{6}element:\s*document\.getElementById\('([^']+)'\)/);
      if (elementMatch) current.elementId = elementMatch[1];
      const hookMatch = line.match(/^\s{6}([A-Za-z0-9_]+):\s*(.+)$/);
      if (hookMatch && KNOWN_HOOKS.has(hookMatch[1])) {
        current.hooks.push({
          key: hookMatch[1],
          line: i + 1,
          expression: hookMatch[2].trim().replace(/,\s*$/, '')
        });
      }

      if (currentDepth <= 0) {
        const bundle = bundleMap.get(current.key);
        current.bundle = bundle || null;
        entries.push(current);
        current = null;
      }
    }

    registryDepth += countChar(line, '{') - countChar(line, '}');
    if (registryDepth <= 0) break;
  }
  return entries.sort((a, b) => a.key.localeCompare(b.key));
}

function formatHookList(entry) {
  return entry.hooks.map(h => `\`${h.key}\``).join(', ') || '(none)';
}

function formatRequiredCoverage(entry) {
  const present = new Set(entry.hooks.map(h => h.key));
  const missing = REQUIRED_METHODS.filter(name => !present.has(name));
  return missing.length ? `Missing: ${missing.map(name => `\`${name}\``).join(', ')}` : 'Complete';
}

function toMarkdown(entries) {
  const componentsRel = toPosix(path.relative(ROOT, COMPONENTS_JS));
  const lines = [];
  lines.push('# Component Contracts');
  lines.push('');
  lines.push(`Generated by \`scripts/generate-component-contracts.js\` from \`${componentsRel}\`.`);
  lines.push('');
  lines.push('This document centralizes the per-component contract expected by `Main.components.registry`, payload persistence defaults, and host element bindings.');
  lines.push('');
  lines.push('## Shared Contract');
  lines.push('');
  lines.push('- `Main` expects each workspace entry to provide `ensure`, `draw`, `getPayload`, `loadFromPayload`, and `createEmptyPayload`.');
  lines.push('- Optional hooks (`activateTab`, `captureRuntimeState`, `applyRuntimeState`, `captureRenderCache`, `restoreRenderCache`, layout helpers) are consumed when present.');
  lines.push('- Payload objects must stay JSON-serializable so session/archive persistence remains stable.');
  lines.push('');
  lines.push('## Registry Coverage');
  lines.push('');
  lines.push('| Component | Tab label | Workspace block | Component file | Host element | Hooks present | Required methods |');
  lines.push('| --- | --- | --- | --- | --- | --- | --- |');
  for (const entry of entries) {
    const bundlePath = entry.bundle?.requirePath
      ? toPosix(path.normalize(path.join('js', 'main', entry.bundle.requirePath)))
      : `js/components/${entry.key}.js`;
    const workspaceRef = `${componentsRel}:${entry.startLine}`;
    const host = entry.elementId ? `\`${entry.elementId}\`` : '(none)';
    lines.push(`| \`${entry.key}\` | ${entry.tabLabel ? `\`${entry.tabLabel}\`` : '(none)'} | \`${workspaceRef}\` | \`${bundlePath}\` | ${host} | ${formatHookList(entry)} | ${formatRequiredCoverage(entry)} |`);
  }
  lines.push('');
  lines.push('## Payload Baselines');
  lines.push('');
  lines.push('| Component | Top-level persisted keys |');
  lines.push('| --- | --- |');
  for (const entry of entries) {
    const payloadKeys = PAYLOAD_BASELINES[entry.key] || ['type', 'data', 'config'];
    lines.push(`| \`${entry.key}\` | ${payloadKeys.map(key => `\`${key}\``).join(', ')} |`);
  }
  lines.push('');
  lines.push('## Notes');
  lines.push('');
  lines.push('- Payload baseline keys are synchronized with `docs/development/state-persistence-schema.md` section "Default Payload Baselines by Component".');
  lines.push('- Regenerate this file after registry or payload contract changes: `npm run docs:component-contracts`.');
  lines.push('- For call-site symbol tracing, use `npm run docs:arch-map`.');
  lines.push('');
  return lines.join('\n');
}

function main() {
  const lines = fs.readFileSync(COMPONENTS_JS, 'utf8').split(/\r?\n/);
  const entries = parseComponentsRegistry(lines);
  const markdown = toMarkdown(entries);
  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, markdown, 'utf8');
  console.log(`Generated ${path.relative(ROOT, OUTPUT_PATH)} (${entries.length} components)`);
}

main();
